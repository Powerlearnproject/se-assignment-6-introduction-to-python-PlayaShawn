# Integer
miaka = 25
print(f"Miaka: {miaka} (Type: {type(miaka)})")# outputs the value and type of miaka.

# Float
pi = 3.14159
print(f"Pi: {pi} (Type: {type(pi)})")#outputs the value and type of 'pi'.

# String
name = "Alice"
print(f"Name: {name} (Type: {type(name)})")#

# Boolean
is_learner = True
print(f"Is Learner: {is_learner} (Type: {type(is_learner)})")

# List
vitamins = ["apple", "banana", "cherry"]
print(f"Vitamins: {vitamins} (Type: {type(vitamins)})")

# Tuple
directions = (10.0, 20.0)
print(f"Coordinates: {directions} (Type: {type(directions)})")

# Dictionary
human = {"name": "Alice", "age": 25}
print(f"Person: {human} (Type: {type(human)})")


# Additional example of using these variables
# Calculating the area of a circle with radius `pi`
radius = 40.9
area = pi * (radius ** 2)
print(f"Area of the circle with radius {radius}: {area}")
